package rectification;
/**
 * Created by Robert on 7/26/2017.
 *
 * Simple class used to store pairs of x-y coords for processing by the rectification tool kit and event handler
 */
public class RectificationPoint
{
    public double x;
    public double y;
    public RectificationPoint(double x, double y)
    {
        this.x = x;
        this.y = y;
    }
}
